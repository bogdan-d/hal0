/** Lane-keyed board view response (shape 1 — lanes object). */
export function makeBoardLanesResponse(includeArchived = false) {
  const LANE_ORDER = [
    'triage', 'todo', 'scheduled', 'ready', 'running',
    'blocked', 'review', 'done',
    ...(includeArchived ? ['archived'] : []),
  ]
  const lanes: Record<string, typeof BOARD_TASKS> = {}
  for (const lane of LANE_ORDER) lanes[lane] = []
  for (const task of BOARD_TASKS) {
    if (!includeArchived && task.status === 'archived') continue
    if (lanes[task.status]) lanes[task.status].push(task)
  }
  return { lanes }
}

